def get_locale():
    from flask import session, request
    return session.get('lang') or request.accept_languages.best_match(['es', 'en']) or 'es'

def _(key):
    lang = get_locale()
    return TRANSLATIONS.get(lang, TRANSLATIONS['es']).get(key, key)
